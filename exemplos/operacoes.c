void main()
{
    int a = 10;
    int b = 6;
    float x = 5.5;
    float y = 2.0;
    
    printf("\n");
    printf("Soma (int): %d\n", a + b);
    printf("\n");
    printf("Subtração (int): %d\n", a - b);
    printf("\n");
    printf("Multiplicação (int): %d\n", a * b);
    printf("\n");
    printf("Divisão (int): %d\n", a / b);
    printf("\n");
    printf("Módulo (resto da divisão): %d\n", a % b);
    printf("\n");

    printf("\n");
    printf("Soma (float): %f\n", x + y);
    printf("\n");
    printf("Subtração (float): %f\n", x - y);
    printf("\n");
    printf("Multiplicação (float): %f\n", x * y);
    printf("\n");
    printf("Divisão (float): %f\n", x / y);
    printf("\n");
}
